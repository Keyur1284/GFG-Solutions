class Solution{
    public:
    
    Node *front = NULL;
    int size = 0, index = 0;
    
    void solve (Node* node)
    {
        if (node->next)
        {
            size++;
            solve(node->next);
        }
        
        if (index == (size + 1)/2)
            return;
            
        int data = front->data;
        front->data = node->data - front->data;
        node->data = data;
        front = front->next;
        index++;
    }
    
    struct Node* modifyTheList(struct Node *head)
    {
        front = head;
        solve(head);
        return head;
    }
};

